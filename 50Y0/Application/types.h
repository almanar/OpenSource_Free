#ifndef _TYPES_H_
	#define _TYPES_H_

	#define NULL  		( 0 )

	#define TRUE  		( 1 )
	#define FALSE 		( 0 )

	#define SUCCESS  	( 0 )
	#define FAILED   	( 1 )

	#define HIGH		( 1 )
	#define LOW			( 0 )

	typedef unsigned char uint8;
	typedef	signed char	  sint8;
	typedef unsigned int  uint32;
	typedef	signed int	  sint32;
	typedef unsigned long uint64;
	typedef	signed long	  sint64;
	
#endif
